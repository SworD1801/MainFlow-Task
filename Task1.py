#Python program to create a list, a
#dictionary, and a set. Perform basic operations
#like adding, removing, and modifying
#elements.

# Create a list
my_list = [1, 2, 3, 4, 5]

# Print the list
print("Original List:", my_list)

# Add an element to the list
my_list.append(6)

# Print the list
print("List After Adding Element:", my_list)

# Remove an element from the list
my_list.remove(3)

# Print the list
print("List After Removing Element:", my_list)

# Modify an element in the list
my_list[0] = 10

# Print the list
print("List After Modifying Element:", my_list)

# Create a dictionary
my_dict = {"apple": 1, "banana": 2, "cherry": 3}

# Print the dictionary
print("Original Dictionary:", my_dict)

# Add an element to the dictionary
my_dict["date"] = 4

# Print the dictionary
print("Dictionary After Adding Element:", my_dict)

# Remove an element from the dictionary
del my_dict["banana"]

# Print the dictionary
print("Dictionary After Removing Element:", my_dict)

# Modify an element in the dictionary
my_dict["apple"] = 5

# Print the dictionary
print("Dictionary After Modifying Element:", my_dict)


# Create a set
my_set = {1, 2, 3, 4, 5}

# Print the set
print("Original Set:", my_set)

# Add an element to the set
my_set.add(6)

# Print the set
print("Set After Adding Element:", my_set)

# Remove an element from the set
my_set.remove(3)

# Print the set
print("Set After Removing Element:", my_set)
